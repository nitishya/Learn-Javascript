let x = 1;

//Relational
console.log(x > 0);
console.log(x >= 1);
console.log(x < 1);
console.log(x <= 1);

//Equality 
console.log(x === 1);
console.log(x !== 1);

//Strict equality (Type + Value)
console.log(1 === 1);
console.log('1' === 1);

//loose equality 
console.log(1 == 1);
console.log('1' == 1);
