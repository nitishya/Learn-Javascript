let x = 10;
let y = 3;

console.log(x + y);
console.log(x - y);
console.log(x * y);
console.log(x / y);
console.log(x % y);  
console.log(x ** y); //x to the power of y

//increment 
console.log(++x);  //value increment first
console.log(x++);  //value print first 
console.log(x); 

//decrement
console.log(--x);  //value increment first
console.log(x--);  //value print first 
console.log(x); 
